package br.com.farmaweb.models;

public class FormaDePagamento {

	private int cod_pagamento;
	private String tipo_pagamento;
	
	public int getCod_pagamento() {
		return cod_pagamento;
	}
	public void setCod_pagamento(int cod_pagamento) {
		this.cod_pagamento = cod_pagamento;
	}
	public String getTipo_pagamento() {
		return tipo_pagamento;
	}
	public void setTipo_pagamento(String tipo_pagamentp) {
		this.tipo_pagamento = tipo_pagamentp;
	}

}
